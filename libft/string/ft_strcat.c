char	*ft_strcat(char *s1, const char *s2)
{
	char *dest_end;

	dest_end = s1;
	while (*dest_end != '\0')
		dest_end++;
	while (*s2 != '\0')
		*dest_end++ = *s2++;
	*dest_end = '\0';
	return (s1);
}
